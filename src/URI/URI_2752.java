package URI;

public class URI_2752 {

	public static void main(String[] args) {
		
		String uri = "AMO FAZER EXERCICIO NO URI";
		
		System.out.printf("<%s>\n", uri);
		System.out.printf("<%30s>\n", uri);
		System.out.printf("<%.20s>\n", uri);
		System.out.printf("<%-20s>\n", uri);
		System.out.printf("<%-30s>\n", uri);
		System.out.printf("<%.30s>\n", uri);
		System.out.printf("<%30.20s>\n", uri);
		System.out.printf("<%-30.20s>\n", uri);
	}
}

